# Rotating index per category to cycle through poems deterministically
_poem_indices = {cat: 0 for cat in POEMS}


def _weather_code_to_category(code):
    """Map WMO weather code to poem category.

    WMO codes: https://open-meteo.com/en/docs
    0: Clear sky
    1-3: Mainly clear / partly cloudy / overcast
    45,48: Fog / depositing rime fog
    51-57: Drizzle
    61-67: Rain
    71-77: Snow
    80-82: Rain showers
    85-86: Snow showers
    95-99: Thunderstorm
    """
    if code is None:
        return "clear"
    if code == 0:
        return "clear"
    if code in (1, 2):
        return "clear"
    if code == 3:
        return "cloudy"
    if code in (45, 48):
        return "fog"
    if 51 <= code <= 57:
        return "rain"
    if 61 <= code <= 67:
        return "rain"
    if 71 <= code <= 77:
        return "snow"
    if 80 <= code <= 82:
        return "rain"
    if 85 <= code <= 86:
        return "snow"
    if 95 <= code <= 99:
        return "storm"
    return "clear"


def _is_night(sunrise_str, sunset_str):
    """Check if current time is night (before sunrise or after sunset)."""
    now = datetime.now()
    try:
        sunrise = datetime.fromisoformat(sunrise_str)
        sunset = datetime.fromisoformat(sunset_str)
        return now < sunrise or now > sunset
    except (ValueError, TypeError):
        # Fallback: night is 21:00-06:00
        hour = now.hour
        return hour >= 21 or hour < 6


def get_poem(weather_code=None, sunrise=None, sunset=None):
    """Get a formatted poem based on weather conditions.

    Args:
        weather_code: WMO weather code from Open-Meteo API.
        sunrise: ISO format sunrise time string.
        sunset: ISO format sunset time string.

    Returns:
        Formatted poem string with separator and attribution.
    """
    # Determine category: night overrides weather if applicable
    if _is_night(sunrise, sunset):
        category = "night"
    else:
        category = _weather_code_to_category(weather_code)

    poems = POEMS[category]
    idx = _poem_indices[category] % len(poems)
    _poem_indices[category] = idx + 1

    text, author = poems[idx]

    return f"‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ\nü™∂ {text}\n  ‚Äî {author}"
